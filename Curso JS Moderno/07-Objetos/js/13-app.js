// Object Literal
const producto = {
  nombre: "Pantalla de 50 pulgadas",
  precio: 5000,
  disponible: true,
};

// Devuelves llaves, en un arreglo
console.log(Object.keys(producto));
// Devuelves los valores, en un arreglo
console.log(Object.values(producto));
// Devuelves llaves y valores, en un arreglo
console.log(Object.entries(producto));
