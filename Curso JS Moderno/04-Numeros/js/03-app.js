let resultado;

// PI
resultado = Math.PI;

// Redondear
resultado = Math.round(2.8);

// Redondear hacia arriba
resultado = Math.ceil(2.2);

// Redondear hacia abajo
resultado = Math.floor(2.9);

// Raiz cuadrada
resultado = Math.sqrt(25);

// Absoluto
resultado = Math.abs(-500);

// Potencia
resultado = Math.pow(8, 3);

// Minimo
resultado = Math.min(1, 2, 3, 4, 5, -4, 20);

// Maximo
resultado = Math.max(1, 2, 3, 4, 5, -4, 20);

// Aleatorio (normalmente son numeros decimales)
resultado = Math.random() * 20;

// Aleatorio dentro de un rango
resultado = Math.floor(Math.random() * 30);

console.log(resultado);
